#include<unistd.h>

void	aide(int i)
{
	i = i + 48;
	write(1, &i, 1);
	i = i - 48;
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			aide(a / 10);
			aide(a % 10);
			write (1, " ", 1);
			aide(b / 10);
			aide(b % 10);
			if (!(a == 98 & b == 99))
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
			b++;
		}
		a++;
	}
}
