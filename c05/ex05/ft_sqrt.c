#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 0 || nb < 0)
		return (0);
	while (i <= nb)
	{
		if (nb == i * i)
			return (i);
		else
			if (i >= 46341)
				return (0);
		i++;
	}
	return (0);
}
