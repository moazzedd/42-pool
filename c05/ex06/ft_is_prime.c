#include<stdio.h>

int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			j++;
			if (j > 1)
				return (0);
		}
		i++;
	}
	if (j == 1)
		return (1);
	return (0);
}
