#include<stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	x;
	int	nb;

	i = 0;
	nb = 0;
	x = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
			i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
		{
			x = x * -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10;
		nb = nb + str[i] - 48;
		i++;
	}
	return (nb * x);
}
